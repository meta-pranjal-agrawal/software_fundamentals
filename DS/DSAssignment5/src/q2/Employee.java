package q2;

public class Employee 
{
	 String name;
	 int age;
	 float salary;
	
	 public Employee(String name,float salary, int age) {
		this.name=name;
		this.salary=salary;
		this.age=age;
	}
	 
	 
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public float getSalary() {
		return salary;
	}
	
	

}
